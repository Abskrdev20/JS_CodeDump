// Switch Case : multiple choices
// can use string also like "apr" default integer match
const month = "apr";

switch (month) {
    case "jan":
        console.log("January");   
        break;
    case "feb":
        console.log(`Feburary`);
        break;
    case "mar":
        console.log(`March`);
        break;
    case "apr":
        console.log(`April`);
        break;
    default:
        break;
}